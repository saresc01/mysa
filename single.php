                    <h5 class="fw-bolder mb-4 mt-5">Soy Wax</h5>
                    <p class="fs-5 mb-4 ">Soy wax is a popular choice for those seeking a clean, slow burn. Derived from soybeans, this wax is not only sustainable but also produces minimal soot, ensuring that the air in your home remains fresh and pure. Plus, soy wax holds fragrance well, providing a consistent scent throw that will fill your space with warmth and comfort.</p>
                    <h5 class="fw-bolder mb-4 mt-5">Coconut Wax</h5>
                    <p class="fs-5 mb-4 ">Coconut wax is a luxurious option that offers a smooth, creamy texture and an exceptional scent throw. This wax is made from the meat of coconuts, a renewable resource, making it one of the most sustainable wax options available. Coconut wax candles burn longer than many others, giving you more hours of enjoyment from each one.</p>
                    <h5 class="fw-bolder mb-4 mt-5">Beeswax</h5>
                    <p class="fs-5 mb-4 ">Beeswax is a timeless choice that has been used for centuries. Known for its natural honey scent and golden hue, beeswax candles emit negative ions when burned, purifying the air by neutralizing pollutants. Our beeswax is sustainably harvested, ensuring that the bees and their habitats are protected.</p>
                    <p class="fs-5 mb-4 mt-4">With Mysa, you don't have to choose between sustainability and variety—we offer both.</p>
                    <h2 class="fw-bolder mb-4 mt-5">Why Choose Natural Over Synthetic?</h2>
                    <p class="fs-5 mb-4 mt-4">When it comes to candles, not all are created equal. Many commercially available candles are made from paraffin, a petroleum byproduct that can release harmful toxins into the air when burned. Here’s why natural wax candles are a far superior choice:</p>
                    <ul class="list-unstyled mb-0">
                      <li><i class="product_font fas fa-check text-success me-2"></i>Health Benefits: Natural waxes like soy, coconut, and beeswax burn cleanly, producing little to no soot or toxic fumes. This is especially important for those with allergies or sensitivities to synthetic fragrances. </li>
                      <li><i class="product_font fas fa-check text-success me-2"></i>Environmental Impact: Paraffin candles contribute to pollution both during their production and when they are burned. In contrast, natural waxes are biodegradable and sourced from renewable resources, making them a more eco-friendly option.</li>
                      <li><i class="product_font fas fa-check text-success me-2"></i>Burn Quality: Natural wax candles typically burn longer and more evenly than synthetic ones, providing better value and a more pleasant experience.</li>
                    </ul>
